func createTestFile(t *testing.T, dir, name, content string) {
	t.Helper()
	path := filepath.Join(dir, name)
	os.MkdirAll(filepath.Dir(path), 0755)
	err := os.WriteFile(path, []byte(content), 0644)
	require.NoError(t, err)
}

func TestWalk_BasicProject(t *testing.T) {
	dir := t.TempDir()

	createTestFile(t, dir, "main.go", "package main\n\nfunc main() {\n\tprintln(\"hello\")\n}\n")
	createTestFile(t, dir, "main_test.go", "package main\n\nfunc TestMain(t *testing.T) {\n}\n")
	createTestFile(t, dir, "README.md", "# Hello\n\nThis is a readme.\n")

	result, err := Walk(dir)
	require.NoError(t, err)

	assert.Equal(t, 3, result.TotalFiles)
	assert.Equal(t, 1, result.TestFiles)
	assert.Equal(t, 2, result.SourceFiles)
	assert.Greater(t, result.TotalLOC, 0)
	assert.Greater(t, result.LanguageLOC["Go"], 0)
	assert.Greater(t, result.LanguageLOC["Markdown"], 0)
}

