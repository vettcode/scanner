func TestWalk_ExcludesGeneratedFiles(t *testing.T) {
	dir := t.TempDir()

	createTestFile(t, dir, "regular.go", "package main\n\nfunc main() {}\n")
	createTestFile(t, dir, "generated.go", "// Code generated by protoc; DO NOT EDIT.\npackage pb\n\ntype Foo struct{}\n")

	result, err := Walk(dir)
	require.NoError(t, err)

	assert.Equal(t, 1, result.TotalFiles)
	assert.Equal(t, "regular.go", filepath.Base(result.Files[0].Path))
}

func TestWalk_MultiLanguage(t *testing.T) {
	dir := t.TempDir()

	createTestFile(t, dir, "main.go", "package main\n\nfunc main() {}\n")
	createTestFile(t, dir, "app.py", "def main():\n    print('hello')\n")
	createTestFile(t, dir, "index.ts", "const x: number = 1;\n")
	createTestFile(t, dir, "style.css", "body { color: red; }\n")

	result, err := Walk(dir)
	require.NoError(t, err)

	assert.Equal(t, 4, result.TotalFiles)
	assert.Greater(t, result.LanguageLOC["Go"], 0)
	assert.Greater(t, result.LanguageLOC["Python"], 0)
	assert.Greater(t, result.LanguageLOC["TypeScript"], 0)
	assert.Greater(t, result.LanguageLOC["CSS"], 0)
}

