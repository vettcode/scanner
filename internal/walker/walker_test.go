func TestWalk_TestFileClassification(t *testing.T) {
	dir := t.TempDir()

	createTestFile(t, dir, "src/app.ts", "export const x = 1;\n")
	createTestFile(t, dir, "src/app.test.ts", "test('x', () => {});\n")
	createTestFile(t, dir, "src/__tests__/helper.ts", "export const h = 1;\n")
	createTestFile(t, dir, "tests/test_app.py", "def test_app(): pass\n")
	createTestFile(t, dir, "spec/app_spec.rb", "describe 'app' do\nend\n")
	createTestFile(t, dir, "tests/AppTest.php", "class AppTest {}\n")

	result, err := Walk(dir)
	require.NoError(t, err)

	testCount := 0
	for _, f := range result.Files {
		if f.IsTest {
			testCount++
		}
	}
	assert.Equal(t, 5, testCount) // app.test.ts, __tests__/helper.ts, test_app.py, app_spec.rb, AppTest.php
}

func TestCountLOC(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "test.txt")

	content := "line1\n\nline3\n\nline5\n"
	os.WriteFile(path, []byte(content), 0644)

	assert.Equal(t, 3, countLOC(path)) // 3 non-blank lines
}

func TestCountLOC_EmptyFile(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "empty.txt")
	os.WriteFile(path, []byte(""), 0644)

	assert.Equal(t, 0, countLOC(path))
}

func TestCountLOC_BlankLinesOnly(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "blanks.txt")
	os.WriteFile(path, []byte("\n\n\n\n"), 0644)

	assert.Equal(t, 0, countLOC(path))
}

func TestWalk_BlankOnlyFileSkipped(t *testing.T) {
	dir := t.TempDir()
	createTestFile(t, dir, "real.go", "package main\n\nfunc main() {}\n")
	createTestFile(t, dir, "blank.go", "\n\n\n")

	result, err := Walk(dir)
	require.NoError(t, err)

	// blank.go has LOC 0 and should be skipped
	assert.Equal(t, 1, result.TotalFiles)
	assert.Equal(t, "real.go", filepath.Base(result.Files[0].Path))
}
