func TestWalk_TestFileClassification(t *testing.T) {
	dir := t.TempDir()

	createTestFile(t, dir, "src/app.ts", "export const x = 1;\n")
	createTestFile(t, dir, "src/app.test.ts", "test('x', () => {});\n")
	createTestFile(t, dir, "src/__tests__/helper.ts", "export const h = 1;\n")
	createTestFile(t, dir, "tests/test_app.py", "def test_app(): pass\n")
	createTestFile(t, dir, "spec/app_spec.rb", "describe 'app' do\nend\n")
	createTestFile(t, dir, "tests/AppTest.php", "class AppTest {}\n")

	result, err := Walk(dir)
	require.NoError(t, err)

	testCount := 0
	for _, f := range result.Files {
		if f.IsTest {
			testCount++
		}
	}
	assert.Equal(t, 5, testCount) // app.test.ts, __tests__/helper.ts, test_app.py, app_spec.rb, AppTest.php
}

